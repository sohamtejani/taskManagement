public enum Category {
	    WORK,         // Work-related tasks
	    PERSONAL,     // Personal tasks (e.g., errands, hobbies)
	    FITNESS,      // Fitness-related tasks (e.g., workout routines)
	    SHOPPING,     // Shopping tasks (e.g., grocery shopping)
	    LEARNING,     // Learning tasks (e.g., studying, courses)
	    FINANCE,      // Finance-related tasks (e.g., budgeting, paying bills)
	    HOUSEHOLD,    // Household tasks (e.g., cleaning, maintenance)
	    HEALTHCARE,   // Healthcare-related tasks (e.g., doctor's appointments)
	    SOCIAL,       // Social-related tasks (e.g., meet-ups, events)
	    OTHER         // Other general tasks that don't fit into the above categories

}
